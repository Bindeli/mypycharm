"""
A função print pode ser executada com aspas simples ou duplas;
'str' = string
String é um tipo de dado primitivo em python;
Tudo que estiver dentro de aspas em python, é chamado de 'string';

"""
print('a função print, pode se utilizar de aspas simples')
print("...ou duplas")
print('123456')
print("Essa é uma 'string' (str)")
# O código foi aberto e fechado com aspas duplas, e foi adicionado outro conteúdo com aspas simples. O código funciona normalmente
print('Essa também é uma "string" (str), porém com aspas simples')
print('Essa é uma \'string\' utilizando um cacactere de escape.')
# O 'caractere de escape', são duas barras invertidas \'  \', colocadas antes das aspas internas do código.
# Sua função é fazer o código saltar a leitura da segunda 'aspas simples', pois o comando também foi escrito com aspas simples.